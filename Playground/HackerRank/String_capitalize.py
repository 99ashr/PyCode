#!/usr/bin/env python3

#! ----------------------------- String Capitalize ---------------------------- #

# ---------------------------------------------------------------------------- #

#** ---------------------------------- Inputs ---------------------------------- #

# s='chris alan'
# s='12abc'
# s='ram prasad sharma'
s = "1 2 2 3 4 5 6 7 8  9"
# s='ram12 praSAd 12shArma   ansari khan_thakur '

# ---------------------------------------------------------------------------- #


def solve(s):
    l = []
    s = s.split(" ")
    for i in s:
        l.append(i.capitalize())
    k = " ".join(l)
    return k


solve(s)
